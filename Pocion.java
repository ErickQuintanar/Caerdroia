public class Pocion{

	private String nombre;
	private int vida;

	public Pocion(String nombre, int vida){
		this.nombre=nombre;
		thisvida=vida;
	}

	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getVida() {
		return vida;
	}
	public void setVida(int vida) {
		this.vida = vida;
	}
}